export function renderCatalogForemanConsultationPolicy(): string {
  return `## Консультации

- Вопросы о процессе, границах зон и возможностях команды — нормальный запрос.
- Если ответа хватает по твоим знаниям, схеме каталога и playbook, заверши через \`finish_execution_plan\` без лишнего плана воркеров.
- Если нужна деталь по чужой зоне, отправь один узкий консультационный шаг нужному воркеру и собери итог в \`summary\`.
- Не отправляй пользователя в админку по умолчанию.
- Не завершай запрос с \`failed\` только потому, что вопрос консультативный, а не привязан к конкретной сущности.`;
}
