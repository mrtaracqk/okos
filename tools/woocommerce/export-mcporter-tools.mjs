#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

function usage() {
  console.error(
    'Usage: node <path-to-generator> <input-json> <worker-toolsets-json> <output-ts-file>'
  );
  process.exit(1);
}

const [, , inputPath, workerToolsetsPath, outputFile] = process.argv;

if (!inputPath || !workerToolsetsPath || !outputFile) {
  usage();
}

const absoluteInputPath = path.resolve(process.cwd(), inputPath);
const absoluteWorkerToolsetsPath = path.resolve(process.cwd(), workerToolsetsPath);
const absoluteOutputFile = path.resolve(process.cwd(), outputFile);

if (!fs.existsSync(absoluteInputPath)) {
  console.error(`Input file not found: ${absoluteInputPath}`);
  process.exit(1);
}

if (!fs.existsSync(absoluteWorkerToolsetsPath)) {
  console.error(`Worker toolsets file not found: ${absoluteWorkerToolsetsPath}`);
  process.exit(1);
}

const raw = fs.readFileSync(absoluteInputPath, 'utf8');
const workerToolsetsRaw = fs.readFileSync(absoluteWorkerToolsetsPath, 'utf8');
let parsed;
let parsedWorkerToolsets;
try {
  parsed = JSON.parse(raw);
} catch (err) {
  console.error(`Failed to parse JSON from ${absoluteInputPath}: ${err.message}`);
  process.exit(1);
}

try {
  parsedWorkerToolsets = JSON.parse(workerToolsetsRaw);
} catch (err) {
  console.error(`Failed to parse JSON from ${absoluteWorkerToolsetsPath}: ${err.message}`);
  process.exit(1);
}

const tools = Array.isArray(parsed.tools) ? parsed.tools : [];
const workerToolsets =
  parsedWorkerToolsets && typeof parsedWorkerToolsets === 'object' ? parsedWorkerToolsets : undefined;

if (!workerToolsets || Array.isArray(workerToolsets)) {
  console.error(`Worker toolsets file must contain an object map: ${absoluteWorkerToolsetsPath}`);
  process.exit(1);
}

fs.mkdirSync(path.dirname(absoluteOutputFile), { recursive: true });
const generatorScriptPath = path.relative(process.cwd(), fileURLToPath(import.meta.url));

const assertToolName = (value) => {
  const name = (value || '').toString().trim();
  if (!name) {
    throw new Error('Encountered tool without a valid name');
  }
  return name;
};

const seen = new Set();
const registry = {};
for (const tool of tools) {
  const toolName = assertToolName(tool.name);
  if (seen.has(toolName)) {
    throw new Error(`Duplicate tool name detected: ${toolName}`);
  }
  seen.add(toolName);
  registry[toolName] = {
    name: toolName,
    description: typeof tool.description === 'string' ? tool.description : '',
    inputSchema:
      tool.inputSchema && typeof tool.inputSchema === 'object'
        ? tool.inputSchema
        : { type: 'object', additionalProperties: true },
  };
}

for (const [workerName, workerTools] of Object.entries(workerToolsets)) {
  if (!Array.isArray(workerTools)) {
    throw new Error(`Worker toolset "${workerName}" must be an array.`);
  }

  for (const toolName of workerTools) {
    if (!registry[toolName]) {
      throw new Error(`Worker "${workerName}" references missing tool "${toolName}" in snapshot.`);
    }
  }
}

const fileContents = `/* eslint-disable */
// This file is generated by ${generatorScriptPath}. Do not edit manually.

export const generatedWooCommerceToolRegistry = ${JSON.stringify(registry, null, 2)} as const;

export const generatedWooCommerceWorkerToolsets = ${JSON.stringify(workerToolsets, null, 2)} as const;

export type GeneratedWooCommerceToolName = keyof typeof generatedWooCommerceToolRegistry;
export type GeneratedWooCommerceWorkerName = keyof typeof generatedWooCommerceWorkerToolsets;
`;

fs.writeFileSync(absoluteOutputFile, `${fileContents}\n`, 'utf8');

console.log(`Generated ${tools.length} tools to ${absoluteOutputFile}`);
